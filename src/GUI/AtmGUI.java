    private void ChangePinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChangePinActionPerformed
        // TODO add your handling code here:
        String oldPin = JOptionPane.showInputDialog(null, "Enter The Old Pin");

        boolean flag = true;
        if (!oldPin.equals(String.valueOf(pincodee))) {
            JOptionPane.showMessageDialog(null, "Incorrect pim");
            flag = false;
        }
        String Pin = JOptionPane.showInputDialog(null, "Enter The new Pin");
        if (Pin.length() != 6) {
            JOptionPane.showMessageDialog(null, "The Pin must Be 6 Numbers");
            flag = false;
        }
        if (flag) {
            try {
                ATM a = new ATM();
                a.changePin(AccountNumber, Integer.parseInt(Pin));
            } catch (IOException ex) {
                Logger.getLogger(AtmGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(AtmGUI.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(AtmGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_ChangePinActionPerformed

    private void AccountInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AccountInfoActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            setVisible(false);
            AccountInfo1.setBounds(0, 0, 400, 350);
            AccountInfo1.setVisible(true);
            String firstName = "", LastName = "", Email = "";
            float Balance = 0;
            int accNumber = 0, age = 0;
            Usr u = new Usr();
            ResultSet GetData = u.GetData(AccountNumber);
            while (GetData.next()) {
                firstName = GetData.getString("FirstName");
                LastName = GetData.getString("lastName");
                Email = GetData.getString("email");
                accNumber = GetData.getInt("AccNum");
                age = GetData.getInt("Age");
                Balance = GetData.getFloat("Balance");
            }
            FirstName.setText(firstName);
            this.LastName.setText(LastName);
            this.Email1.setText(Email);
            jLabel5.setText("Account Number : " + accNumber);
            Age.setText(String.valueOf(age));
            balance.setText(String.valueOf(Balance));
        } catch (IOException | SQLException ex) {
            Logger.getLogger(UserLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_AccountInfoActionPerformed

