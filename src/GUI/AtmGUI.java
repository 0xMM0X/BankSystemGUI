    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        try {
            // TODO add your handling code here:
            String acc = AccNumber.getText().toString();
            String pin = PinCode.getText().toString();
            ATM a = new ATM();
            int accNumber = Integer.parseInt(acc);
            int pinCode = Integer.parseInt(pin);
            if (a.AtmLogin(accNumber, pinCode)) {
                ATM.setBounds(0, 0, 338, 338);
                ATM.setVisible(true);
                setVisible(false);
                AccountNumber = accNumber;
                pincodee = pinCode;
            } else {
                JOptionPane.showMessageDialog(null, " Incorrect Account Number Or Pin Code");
            }
        } catch (IOException | SQLException ex) {
            Logger.getLogger(AtmGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_LoginActionPerformed

